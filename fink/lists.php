<h1>Mailing Lists</h1>

<p>The project has three mailing lists, hosted by SourceForge:</p>
<ul>
<li><a href="http://lists.sourceforge.net/lists/listinfo/fink-announce">fink-announce</a>
(<a href="http://www.geocrawler.com/redir-sf.php3?list=fink-announce">Archives</a>)<br>
This is a moderated, low-traffic list for release announcements.</li>
<li><a href="http://lists.sourceforge.net/lists/listinfo/fink-users">fink-users</a>
(<a href="http://www.geocrawler.com/redir-sf.php3?list=fink-users">Archives</a>)<br>This
is the general talk mailing list for Fink users. If you have problems,
this is the place to ask.</li>
<li><a href="http://lists.sourceforge.net/lists/listinfo/fink-devel">fink-devel</a>
(<a href="http://www.geocrawler.com/redir-sf.php3?list=fink-devel">Archives</a>)<br>This
is the mailing list for package maintainers and developers. If you
want to package your favourite software for Fink, this is the place to
go.</li>
</ul>
<p>Note that all lists have a size limit of 40K. Please don't use
HTML- or RTF-formatted messages or attachments.</p>


